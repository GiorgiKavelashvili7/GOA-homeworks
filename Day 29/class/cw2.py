# existing queue:
queue = ["John", "Amy", "Bob", "Adam"] # მოცემული გვაქვს სია შემდეგი მნიშვნელობებით: John, Amy, Bob, Adam.

# Take input from the user.
input = input("Please input something: ") # კომპიუტერი მომხმარებელს სთოხვს რომ შემოიტანოს ტექსტი ტერმინალში.

# Add the input item to the end of the queue.
queue.append(input) # კომპიუეტერი ამატებს მომხმარებლის მიერ შემოტანილ ტექსტს სიის ბოლოში.

# Output the resulting list.
print(queue) # კომპიუეტერი იტანს სიას ტერმინალში.