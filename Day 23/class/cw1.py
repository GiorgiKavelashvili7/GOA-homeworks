# შექმენით ფუნქცია, რომელიც გამოიტანს x და y ჯამს, მოახდინეთ ოპერაცია შემდეგ ოპერატორებზე: +, -. *. //

def sum(x, y):
    print(x + y)

def difference(x, y):
    print(x - y)

def matproduct(x, y):
    print(x * y)

def divide(x, y):
    print(x // y)    


sum(100, 100)
difference(200, 100)    
matproduct(10, 4)
divide(50, 25)