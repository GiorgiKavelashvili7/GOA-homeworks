# ახსენით კომენტარების დახმარებით int(), str(), type() და input() ფუნქცია.

# 1.- int() ფუნქცია ხდის ყველა იმ რიცხვს ინტეგერად რომლებიც ფრჩხილებშია მოცემული
# 2.- str() ფუნქცია ხდის ყველა იმ სიტყვას სტრინგად რომლებიც ფრჩხილებშია ჩასმული
# 3.- type() ფუნქცია იყვანს  ფჩხილებში მოცემულ სიტყვის ან ცვლადის მნიშვნელობაის კოდის ტიპს
# 4.- input() ფუნქცია სთხოვს მომხმარებელს შეიტანოს ინფორმაცია ტერმინალში 